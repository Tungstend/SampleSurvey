    ws = wb.active  # 或者 ws = wb['SheetName']

    # 写入面积数组到第5列，从第2行开始（假设第1行为标题）
    for i, value in enumerate(data, start=2):
        ws.cell(row=i, column=5).value = value  # 第五列

    # 写入列标题（可选）
    ws.cell(row=1, column=5).value = "面积-校准"

    # 保存结果
    wb.save(output_path)
    print("写入完成，保存为", output_path)

